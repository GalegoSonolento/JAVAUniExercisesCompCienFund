public class Carro {
    private String modelo;
    private int idade;
    private String cor;
    private double valor;
    private double kmRodados;
    
    public Carro (String modelo, int idade, String cor, double valor, double kmRodados) {
        this.modelo = modelo;
        this.idade = idade;
        this.cor = cor;
        this.valor = valor;
        this.kmRodados = kmRodados;
    }
}