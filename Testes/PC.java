public class PC {
    private String modelo;
    private String marca;
    private double valor;
    private int idade;
    private String cor;
    
    PC (String modelo, String marca, double valor, int idade, String cor) {
        this.modelo = modelo;
        this.marca = marca;
        this.valor = valor;
        this.idade = idade;
        this.cor = cor;
    }
}