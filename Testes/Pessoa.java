public class Pessoa {
    private String nome;
    private int idade;
    private boolean adulto;
    private String sexo;
    private char primeiraLetraNome;
    
    public Pessoa (String nome, int idade, boolean adulto, String sexo, char primeiraLetraNome) {
        this.nome = nome;
        this.idade = idade;
        this.adulto = adulto;
        this.sexo = sexo;
        this.primeiraLetraNome = primeiraLetraNome;
        
    }
}